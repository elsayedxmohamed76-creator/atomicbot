import type { AtomicBotConfig } from "./config.js";

export function ensurePluginAllowlisted(cfg: AtomicBotConfig, pluginId: string): AtomicBotConfig {
  const allow = cfg.plugins?.allow;
  if (!Array.isArray(allow) || allow.includes(pluginId)) {
    return cfg;
  }
  return {
    ...cfg,
    plugins: {
      ...cfg.plugins,
      allow: [...allow, pluginId],
    },
  };
}
